class Anonymous {

	static void sum(int a[])
	{
		int total=0;
		for(int l:a)
		{
			total=total+l;
		
		}
		  System.out.println("total of Array elements is " +total);
	}
	
	public static void main(String []args)
	{
		Anonymous.sum(new int[] {10,14,12,17,74});
	}
}




     
          
            
                

        
         
 


